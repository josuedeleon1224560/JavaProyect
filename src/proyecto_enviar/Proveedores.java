    private void btnMODIFICARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMODIFICARActionPerformed
        
// TODO add your handling code here:

        String idProveedor = txtIdProveedor.getText();
        String Nombre = txtNombre.getText();
        String Telefono = txtTelefono.getText();
        String Direccion = txtDireccion.getText();
        String Correo = txtCorreoElectronico.getText();
        String Documento = txtDocumento.getText();

        if (lblIDProveedores.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun registro..");
            return;
        }

        int esteIndice = Integer.parseInt( lblIDProveedores.getText());

          try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("UPDATE proveedores SET Nombre=(?), Telefono=(?), Dirección=(?), Correo=(?), Documento=(?) WHERE idProveedores=(?)");  

             stmt.setString(1, Nombre);
            stmt.setString(2, Telefono);
            stmt.setString(3, Direccion);
            stmt.setString(4, Correo);
            stmt.setString(5, Documento);
                        stmt.setString(6, idProveedor);
                        
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Proveedor modificado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
        //        arrId.add(vIdCliente);
        arrNombre.set(esteIndice, txtNombre.getText());
        arrTelefono.set(esteIndice, txtTelefono.getText());
        arrDireccion.set(esteIndice, txtDireccion.getText());
        arrCorreoElectronico.set(esteIndice, txtCorreoElectronico.getText());
        arrDocumento.set(esteIndice, txtDocumento.getText());

        //JOptionPane.showMessageDialog(rootPane, "Registro Modificado...");
        this.Limpiar();
    }//GEN-LAST:event_btnMODIFICARActionPerformed

