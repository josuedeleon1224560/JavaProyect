    private void btnGUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGUARDARActionPerformed
        // TODO add your handling code here:
        String idProveedor = txtIdProveedor.getText();
        String Nombre = txtNombre.getText();
        String Telefono = txtTelefono.getText();
        String Direccion = txtDireccion.getText();
        String Correo = txtCorreoElectronico.getText();
        String Documento = txtDocumento.getText();

        int opcion = JOptionPane.showConfirmDialog(null,"¿Desea grabar este nuevo registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION) {
            return;
        }

        arrIdProveedor.add(idProveedor);
        arrNombre.add(Nombre);
        arrTelefono.add(Telefono);
        arrDireccion.add(Direccion);
        arrCorreoElectronico.add(Correo);
        arrDocumento.add(Documento);

        lblCantidadReg.setText(Integer.toString(arrIdProveedor.size()));
        
                try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("INSERT INTO proveedores VALUES (?, ?, ?, ?, ?, ?)"); 
            stmt.setString(1, idProveedor);
            stmt.setString(2, Nombre);
            stmt.setString(3, Telefono);
            stmt.setString(4, Direccion);
            stmt.setString(5, Correo);
            stmt.setString(6, Documento);
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Proveedor agregado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }

        this.Limpiar();
    }//GEN-LAST:event_btnGUARDARActionPerformed

