    private void btnBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBUSCARActionPerformed
        // TODO add your handling code here:
        int Buscar = arrId.indexOf(txtIdCliente.getText());
        if (Buscar != -1){
            
            txtNombre.setText((String) arrNombre.get(Buscar));
            txtApellido.setText((String) arrApellido.get(Buscar));
            txtDireccion.setText((String) arrDireccion.get(Buscar));
            txtCorreoElectronico.setText((String) arrCorreoElectronico.get(Buscar));
            txtTelefono.setText((String) arrTelefono.get(Buscar));
            txtNit.setText((String) arrNit.get(Buscar));

            String contenido = (String) arrSexo.get(Buscar);
            if (contenido=="Femenino"){
                cboSexo.setSelectedIndex(0);
            } else {
                cboSexo.setSelectedIndex(1);
            }
            lblIDClientes.setText(Integer.toString (Buscar));
        } else{
            lblIDClientes.setText("-1");
            JOptionPane.showMessageDialog(rootPane, "Este cliente no esxiste...");
            lblIDClientes.setText("-1");
        }
    }//GEN-LAST:event_btnBUSCARActionPerformed

