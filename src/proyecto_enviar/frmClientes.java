    private void btnGUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGUARDARActionPerformed
        // TODO add your handling code here:
        String vNombre = txtNombre.getText();
        String vApellido = txtApellido.getText();
        String vDireccion = txtDireccion.getText();
        String vCorreoElectronico = txtCorreoElectronico.getText();
        String vTelefono = txtTelefono.getText();
        String vNit = txtNit.getText();
        String vIdCliente = txtIdCliente.getText();
        String vDocumento = txtDocumento.getText();
        String vSexo = (String) cboSexo.getSelectedItem();

        int opcion = JOptionPane.showConfirmDialog(null, "Desea guardar registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION) {
            return;
        }
        arrId.add(vIdCliente);
        arrNombre.add(vNombre);
        arrApellido.add(vApellido);
        arrDireccion.add(vDireccion);
        arrCorreoElectronico.add(vCorreoElectronico);
        arrTelefono.add(vTelefono);
        arrNit.add(vNit);
        arrSexo.add(vSexo);
        arrDocumento.add(vDocumento);
        lblIDClientes.setText(Integer.toString(arrNombre.size()));

        JOptionPane.showMessageDialog(null, "Registro Guardado..." );
        lblCantReg.setText(Integer.toString(arrId.size()));
        
         try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("INSERT INTO clientes VALUES (?, ?, ?, ?, ?, ?, ?, ?)"); 
            stmt.setString(1, vIdCliente);
            stmt.setString(2, vNombre);
            stmt.setString(3, vApellido);
            stmt.setString(4, vDireccion);
            stmt.setString(5, vTelefono);
            stmt.setString(6, vNit);
            stmt.setString(7, vSexo);
            stmt.setString(8, vDocumento);
            
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Cliente agregado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
        this.Limpiar();

    }//GEN-LAST:event_btnGUARDARActionPerformed

