    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
         String IdRol = txtIdRol.getText();
          String NombreRol = txtNombreRol.getText();
         
         
        if (lblRegistro.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun Rol..");
            return;
        }
        int esteIndice = Integer.parseInt( lblRegistro.getText());

                try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("UPDATE rol SET nombre=(?) WHERE idRol=(?)");  
            stmt.setString(2, IdRol);
            stmt.setString(1, NombreRol);
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Rol modificado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
        arrIdRol.set(esteIndice, txtIdRol.getText());
        arrNombreRol.set(esteIndice, txtNombreRol.getText());


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (lblRegistro.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscano ninguna Categoría..");
            return;
        }
        int esteIndice = Integer.parseInt( lblRegistro.getText());
        
        String indiceBD = (String) arrIdRol.set(esteIndice, txtIdRol.getText());
        
        try
        {
            PreparedStatement stmt = null;
            
            Connection con= Conectar.getConexion();
             stmt = con.prepareStatement("DELETE FROM rol WHERE idRol = ?");
             
             stmt.setString(1, indiceBD);
                     
                     stmt.execute();
                     
                     JOptionPane.showMessageDialog(null, "Rol eliminado...");
        }catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
                
        arrIdRol.remove(esteIndice);
        arrNombreRol.remove(esteIndice);

        lblCantidadReg.setText(Integer.toString(arrIdRol.size()));

        this.Limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

