    private void btnIdRolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIdRolActionPerformed
        // TODO add your handling code here:
        int indice = arrIdRol.indexOf(txtIdRol.getText());
        if (indice != -1) {
            txtIdRol.setText((String) arrIdRol.get(indice));
            txtNombreRol.setText((String) arrNombreRol.get(indice));

            lblRegistro.setText(Integer.toString(indice));
        }else {
            lblRegistro.setText("-1");
            JOptionPane.showMessageDialog(rootPane, "Este Rol No existe...");
        }
    }//GEN-LAST:event_btnIdRolActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        boolean existeCódigo = arrIdRol.contains(txtIdRol.getText());
        if (existeCódigo) {
            JOptionPane.showMessageDialog(rootPane, "Este IdRol y Rol ya existe...");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Este IdRol y Rol No existe...");
        }
        lblRegistro.setText("-1");
    }//GEN-LAST:event_btnBuscarActionPerformed
 private void Limpiar()
        {
            txtIdRol.setText(null);
            txtNombreRol.setText(null);
            txtIdRol.requestFocus();
        }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        String IdRol = txtIdRol.getText();
        String NombreRol = txtNombreRol.getText();

        int opcion = JOptionPane.showConfirmDialog(null,"¿Desea grabar este nuevo registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION) {
            return;
        }

        arrIdRol.add(IdRol);
        arrNombreRol.add(NombreRol);
        lblCantidadReg.setText(Integer.toString(arrIdRol.size()));
  

        try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("INSERT INTO rol VALUES (?, ?)"); 
            stmt.setString(1, IdRol);
            stmt.setString(2, NombreRol);
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Usuario agregado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
        this.Limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

