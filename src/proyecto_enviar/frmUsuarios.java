    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int indice = arrNoUsuario.indexOf(txtNoUsuario.getText());

        boolean existeCódigo = arrNoUsuario.contains(txtNoUsuario.getText());
        if (existeCódigo) {
            JOptionPane.showMessageDialog(rootPane, "Se encuentra disponible el usuario con su ID:...");

            if (indice != -1) {
                txtNoUsuario.setText((String) arrNoUsuario.get(indice));
                txtIdRol.setText((String) arrIdRol.get(indice));
                txtContraseña.setText((String) arrContraseña.get(indice));
                txtNombreUsuario.setText((String) arrUsuario.get(indice));

                lblRegistro.setText(Integer.toString(indice));
            }else {
                lblRegistro.setText("-1");
                JOptionPane.showMessageDialog(rootPane, "Este Usuario No existe...");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Este No. Usuario y Usuario No existe...");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (lblRegistro.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscano ninguna Categoría..");
            return;
        }
        int esteIndice = Integer.parseInt( lblRegistro.getText());
         String indiceBD = (String) arrNoUsuario.set(esteIndice, txtNoUsuario.getText());
        
          try
        {
            PreparedStatement stmt = null;
            
            Connection con= Conectar.getConexion();
             stmt = con.prepareStatement("DELETE FROM usuario WHERE idUsuario = ?");
             
             stmt.setString(1, indiceBD);
                     
                     stmt.execute();
                     
                     JOptionPane.showMessageDialog(null, "Usuario eliminado...");
        }catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
          
        arrNoUsuario.remove(esteIndice);
        arrIdRol.remove(esteIndice);
        arrContraseña.remove(esteIndice);
        arrUsuario.remove(esteIndice);

        lblCantidadReg.setText(Integer.toString(arrNoUsuario.size()));        
        this.Limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        String NoUsuario = txtNoUsuario.getText();
        String Usuario = txtNombreUsuario.getText();
        String Contraseña = txtContraseña.getText();
        String IdRol = txtIdRol.getText();

        int opcion = JOptionPane.showConfirmDialog(null,"¿Desea grabar este nuevo registro" + "?");
        if (opcion == JOptionPane.NO_OPTION || opcion == JOptionPane.CANCEL_OPTION) {
            return;
        }

        arrUsuario.add(Usuario);
        arrContraseña.add(Contraseña);
        arrIdRol.add(IdRol);
        arrNoUsuario.add(NoUsuario);

          try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("INSERT INTO usuario VALUES (?, ?, ?, ?)"); 
            stmt.setString(1, NoUsuario);
            stmt.setString(2, Usuario);
            stmt.setString(3, Contraseña);
            stmt.setString(4, IdRol);
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Usuario agregado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        
        lblCantidadReg.setText(Integer.toString(arrNoUsuario.size()));    
        this.Limpiar();

    }//GEN-LAST:event_btnGuardarActionPerformed

