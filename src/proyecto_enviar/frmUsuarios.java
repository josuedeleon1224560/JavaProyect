    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        
        String NoUsuario = txtNoUsuario.getText();
        String Usuario = txtNombreUsuario.getText();
        String Contraseña = txtContraseña.getText();
        String IdRol = txtIdRol.getText();
         
         
        
        if (lblRegistro.getText().equals("-1")){
            JOptionPane.showMessageDialog(rootPane, "No ha buscado ningun Usuario..");
            return;
        }
        int esteIndice = Integer.parseInt( lblRegistro.getText());

        arrUsuario.set(esteIndice, txtNombreUsuario.getText());
        arrContraseña.set(esteIndice, txtContraseña.getText());
        arrIdRol.set(esteIndice, txtIdRol.getText());
        arrNoUsuario.set(esteIndice, txtNoUsuario.getText());
 try{
            PreparedStatement stmt = null;
            Connection con = Conectar.getConexion();
            stmt = con.prepareStatement("UPDATE usuario SET nombre=(?), contraseña=(?), idRol=(?) WHERE  idUsuario=(?)");  
            stmt.setString(1, Usuario);
            stmt.setString(2, Contraseña);
            stmt.setString(3, IdRol);
            stmt.setString(4, NoUsuario);
            
            int retorno = stmt.executeUpdate();
            if (retorno>0)
                JOptionPane.showMessageDialog(null, "Usuario modificado");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    

    }//GEN-LAST:event_btnActualizarActionPerformed
